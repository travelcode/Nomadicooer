namespace Nomadicooer.Xsd
{

    /// <summary>
    /// 空白符处理形式
    /// </summary>
    public enum WhiteSpace
    {
        /// <summary>
        /// 维持
        /// </summary>
        Preserve,
        /// <summary>
        /// 移除
        /// </summary>
        Replace,
        /// <summary>
        /// 其他空白符替换为空格
        /// </summary>
        Collapse
    }
}
